    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $employee = User::find($id);
        return view('Backend.pages.employee.edit', compact('employee'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        // Validation Data
        $request->validate([
            'status' => 'required'
        ]);

        $data = User::find($id);
        $data->status = $request->status;

        $data->update();
        return redirect()->back()->with('Status', 'Employee Aprroved Successfully.');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $employee = User::find($id);

        $destination = 'uploads/employee_images/'. $employee->image; // get destination 
        if (File::exists( $destination)) {
            File::delete($destination);
        }
        $employee->delete();
        return redirect()->back()->with('Status', 'Employee Deleted Successfully.');
    }
}
